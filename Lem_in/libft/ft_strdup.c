#include "libft.h"

char	*ft_strdup(const char *s1)
{
	t_string	s2;
	size_t		i;

	s2 = (t_string)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!s2 | !s1)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
