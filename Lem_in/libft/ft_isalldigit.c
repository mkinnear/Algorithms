#include "libft.h"

int		ft_isalldigit(char *s)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] && ft_isdigit(s[i]))
			i++;
		if (s[i])
			return (0);
		else
			return (1);
	}
	return (0);
}
